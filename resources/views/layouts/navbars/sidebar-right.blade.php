      <h3>
          Los pasos a seguir
      </h3>
      <div class="nav-item">
          Lo ideal sería dedicar varias vueltas a cada tema:
          <ul>
              <li>Primero sería la lectura: sentarse a leer tranquilamente. Ver de qué va el tema, hacernos una idea global...</li>
              <li>El segundo paso sería el subrayado: lápices de colores, subrayadores… lo que más nos guste. Se aconseja subrayar entre un 25 y 30% del texto.</li>
              <li>El tercer paso es el resumen: supone el primer esfuerzo real. Ya no resulta quizá tan entretenido, pero es necesario. Si vamos muy pillados de tiempo y tenemos habilidad, quizá podamos pasar del segundo al cuarto paso. </li>
              <li>Cuarto paso  hacer esquemas.  Hacer esquemas nos refuerza ya que nos ayuda a recordar palabras e ideas clave con relativa facilidad. Utiliza tus propias palabras. Será más fácil memorizar luego. Además, nos permite más adelante repasar mucho más rápido y recordar los temas en un par de vistazos.</li>
              <li>Fase de la memorización. Hay que aprenderse esos esquemas. Es la más dura, puede parecer imposible y desesperante, pero recuerda: muchos otros antes que tú lo estudiaron, lo aprendieron, aprobaron el examen y hoy en día tienen su plaza, así que si ellos pudieron, también puedes tú.</li>
              <li>Por útimo: el repaso. Repasar, repasar y repasar, para no olvidar lo aprendido.</li>
          </ul>
      </div>
      <div class="nav-item">
          <h3>¿Cómo estudiar?</h3>
          <ul>
              <li>Comprobar el material de estudio. Incluso si el temario lo adquieres a través de una academia, antes de comenzar un tema repasa los apartados de dicho tema con los puntos que aparecen en el temario publicado en el BOE que hay que estudiar. A veces las academias se saltan algún punto, sorprendente pero cierto.</li>
              <li>Planifica: No improvises nunca. Con ayuda de un calendario, agenda o planificador, decide qué temas vas a estudiar, en qué orden y cuándo. La mayoría de los expertos aconsejan empezar por los de dificultad media, continuar con los más difíciles y acabar con los fáciles.</li>
              <li>Intenta tomarte el estudio como si de un trabajo se tratase, con un horario establecido y marcando las actividades a realizar cada día. Es aconsejable que te pongas objetivos semanales y que midas tu avance cada cierto tiempo. De este modo sabrás cuanto tiempo puedes dedicar a cada tema y comprobar tus puntos fuertes y débiles.</li>
              <li>Independientemente de que estés trabajando o no, lo más recomendable será que inicies tus sesiones con baja intensidad. Empieza invirtiendo unas pocas horas para evitar la fatiga. A medida que pase el tiempo, podrás ir incrementando la duración de las sesiones hasta alcanzar una jornada completa (si es que tus circunstancias te lo permiten).</li>
              <li>No olvides el descanso: un error de novato a la hora de organizar todo y planificar el estudio suele ser olvidar los descansos. Las oposiciones son como un trabajo y en los trabajos también se descansa, así que programa al menos un día libre a la semana para despejar la mente. Si no acabarás saturado y tu rendimiento empezará a caer de manera notable.</li>
              <li>Busca ayuda preparar oposiciones es duro, más todavía si es la primera vez que lo haces. Si es así (o incluso si no lo es y te lo puedes permitir), te recomendamos apuntarte a una academia. </li>
          </ul>
      </div>
        <div class="nav-item">
            <h3>
                ¿Qué hacer para rebajar los nervios?
            </h3>
            <ul>
                <li>Visita el lugar del examen: no hay nada más estresante que enfrentarse a lo desconocido. Evíta esa angustia innecesaria visitando el lugar del examen con varios días, incluso semanas de antelación. Sabrás de antemano dónde está todo: el aula, los lavabos, el punto de información… Y lo que es más importante, cuando llegue el día clave sentirás que dominas la situación. Además, no te perderás justo ese mismo día. </li>
                <li>Relacionarte con otros opositores también puede ser positivo y te ayudará a estar atento a cualquier novedad. Hay grupos de Facebook, Telegram, What’s up… Búscalos, únete a ellos y quédate en los que creas que más te van a aportar.</li>
            </ul>
        </div>
        <div class="nav-item">
            <h3>
                Otros consejos
            </h3>
            <ul>
                <li>Haz simulacros cuando ya lleves una parte prudencial estudiada, hacer simulacros antes de estudiar no tiene mucho sentido.</li>
                <li>Busca técnicas de test por internet: hay muchas y muy buenas. Puede que no siempre funcionen, pero sí en un alto porcentaje de loso casos.</li>
                <li>Revisa el DNI, que esté en vigor y no caduque, si es así renuévalo antes.</li>
                <li>Lleva al examen: lápices, boles, botella de agua, reloj.</li>
                <li>“Pinta” tu examen: en la hoja de preguntas, si se te permite (suele ser que sí), subraya las palabras clave.</li>
                <li>En el examen piensa la respuesta correcta antes de leer las posibles opciones: hará que te hagan dudar menos.</li>
                <li>Si dudas entre dos, valora arriesgarte. Si dudas entre 3 o más, las posibilidades de contestar mal ya son demasiado altas.</li>
                <li>Y finalmente, ¡visitanos! Ve los videos de nuestro <a href="https://www.youtube.com/channel/UC6eK4uuUTeFMWcBYJhZxhpA">canal</a> de los puntos que más dudes, haz nuestros <a href="exams">exámenes</a> interactivos, controla tu tiempo con nuestro <a href="pomodoro">pomodoro</a> y comprueba el resto de <a href="formacion-continuada">recursos</a> que ponemos a tu disposición. ¡Mucha suerte!</li>
            </ul>
